package com.xmut.zhihu.entity;

public class AttentionUser {
    private Integer id;

    private Integer uid;

    private Integer attentioUid;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public Integer getAttentioUid() {
        return attentioUid;
    }

    public void setAttentioUid(Integer attentioUid) {
        this.attentioUid = attentioUid;
    }
}